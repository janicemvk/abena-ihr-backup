# Utility functions for data validation
def validate_patient_data(patient: PatientProfile) -> bool:
    """Validate patient data completeness and correctness"""
    try:
        # Check required fields
        required_fields = ['patient_id', 'age', 'gender']
        for field in required_fields:
            if not getattr(patient, field):
                return False
        
        # Check data types and ranges
        if not isinstance(patient.age, int) or patient.age < 0:
            return False
        
        if patient.gender not in ['male', 'female', 'other']:
            return False
        
        # Check for valid pain scores
        if patient.pain_scores:
            for score in patient.pain_scores:
                if not (0 <= score <= 10):
                    return False
        
        return True
    
    except Exception:
        return False


def validate_treatment_plan(treatment: TreatmentPlan) -> bool:
    """Validate treatment plan completeness and correctness"""
    try:
        # Check required fields
        if not treatment.treatment_id or not treatment.treatment_type:
            return False
        
        # Check duration
        if not isinstance(treatment.duration_weeks, int) or treatment.duration_weeks <= 0:
            return False
        
        # Check treatment type
        valid_types = ['pharmacological', 'behavioral', 'combined', 'lifestyle']
        if treatment.treatment_type not in valid_types:
            return False
        
        return True
    
    except Exception:
        return False


